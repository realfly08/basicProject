package com.bp.po;

public class SignatureReq {
    String url;
    String noicestr;
    String timestamp;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getNoicestr() {
        return noicestr;
    }

    public void setNoicestr(String noicestr) {
        this.noicestr = noicestr;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }
}
